// Sparse connections between nearby dots to hint at continent shapes
const LINKS: [number, number, number, number][] = [
  [10,18, 12,16],[14,15, 16,14],[12,24, 10,22],[22,36, 24,36],
  [46,26, 48,26],[50,28, 52,28],[52,34, 50,34],[52,40, 50,40],
  [58,22, 60,22],[66,20, 68,20],[80,20, 82,20],[84,14, 86,14],
  [84,40, 86,40],[86,42, 88,42],[48,10, 50,10],[44,16, 46,16],
];

interface Props {
  width: number;
  height: number;
  dotColor?: string;
  lineColor?: string;
  dotRadius?: number;
  style?: ViewStyle | any;
}

export const WorldMapSVG: React.FC<Props> = ({
  width,
  height,
  dotColor = 'rgba(0,213,152,0.35)',
  lineColor = 'rgba(0,213,152,0.12)',
  dotRadius = 1.8,
  style,
}) => {
  const toX = (pct: number) => (pct / 100) * width;
  const toY = (pct: number) => (pct / 100) * height;

  return (
    <Svg width={width} height={height} style={style}>
      {/* Connection lines */}
      <G>
        {LINKS.map(([x1p, y1p, x2p, y2p], i) => (
          <Line
            key={`l${i}`}
            x1={toX(x1p)} y1={toY(y1p)}
            x2={toX(x2p)} y2={toY(y2p)}
            stroke={lineColor}
            strokeWidth={0.8}
          />
        ))}
      </G>
      {/* Continent dots */}
      <G>
        {DOTS.map(([xp, yp], i) => (
          <Circle
            key={`d${i}`}
            cx={toX(xp)}
            cy={toY(yp)}
            r={dotRadius}
            fill={dotColor}
          />
        ))}
      </G>
    </Svg>
  );
};
